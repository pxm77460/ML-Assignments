radius = 10  # Created a variable radius
area = 3.14 * radius ** 2  # two * sign means exponent of power

#  Printing radius and area of a circle using Formatting method
# f"" used to format and display the data . this will take the data(variables) by using {} operator
# we can insert the variable values inside {} and it will replace it with the original values when displaying

print(f"The area of a circle with radius {radius} is {area} meters square.")  