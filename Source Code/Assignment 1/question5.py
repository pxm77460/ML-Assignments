# Initialised variable r and Taking input from the user using input() function and 
# coverting the input string into float because area can be decimal
radius = float(input("Enter radius : "))  
# Calculating area of  a circle
_area_of_circle = 3.14 * radius * radius  # Formula to calculate are of circle

print("Area of Circle: ", _area_of_circle)

# Calculating circumference of a Circle
_circum_of_circle_ = 2 * 3.14 * radius  # Formula to calculate Area of circle
print("Circumference of a Circle: ", _circum_of_circle_)





















